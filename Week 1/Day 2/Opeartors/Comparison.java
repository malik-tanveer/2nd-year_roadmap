public class Comparison {

    public static void main(String[] args) {
        System.out.println("Hello i am Comparison");

        int a = 10, b = 20;
        // a b ka brabr nhi is liya flase
        System.out.println(a == b); // false
        // a != ya a jaya to not equal ya braber hai ya true ko false or false ko true
        System.out.println(a != b); // true
        // a choota hia b sa to false
        System.out.println(a > b); // false
        // b bra hai a sa true
        System.out.println(a < b); // true
        // a >= mean ka ya bra hai to true
        System.out.println(a >= 10); // true
        // ya chota hia to false
        System.out.println(b <= 10); // false

    }
}