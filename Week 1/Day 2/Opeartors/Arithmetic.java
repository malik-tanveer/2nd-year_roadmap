public class Arithmetic {
    public static void main(String[] args) {

        System.out.println("Arithmetic:");
        // Plus +
        int a = 10;
        int b = 30;
        System.out.println("10 + 30: " + (a + b));

        // Minus -
        int c = 50;
        int d = 40;
        System.out.println("50 - 40:" + (c - d));

        // Multiplication
        int e = 5;
        int f = 4;
        System.out.println("5 * 4: " + (e * f));

        // Divide /
        int div = 10;
        int div_1 = 4;
        System.out.println("10 / 4:" + (div / div_1));

        // Moudules %

        int mod = 20;
        int model = 3;
        System.out.println("20 % 3: " + mod % model);
    }
}