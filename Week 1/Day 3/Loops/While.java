public class While {

    public static void main(String[] args) {
        System.out.println("While Loop");
        int i = 1;
        while (i <= 5) {
            System.out.println("Number :" + i);
            i++;
        }

        // Reversing Counting
        System.out.println("Reversing Counting & Loop");
        int j = 5;
        while (j >= 1) {
            System.out.println("Number: " + j);
            j--;
        }
    }
}